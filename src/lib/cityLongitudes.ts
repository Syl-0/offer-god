/**
 * 获取城市经度
 */
export function getCityLongitude(province: string, city?: string): number | null {
  // 先尝试精确匹配城市名
  if (city) {
    // 去掉"市"、"地区"、"自治州"等后缀
    const cleanCity = city.replace(/市$|地区$|自治州$|盟$|县$/g, '');
    if (CITY_LONGITUDES[cleanCity]) {
      return CITY_LONGITUDES[cleanCity];
    }
    // 尝试完整城市名
    if (CITY_LONGITUDES[city]) {
      return CITY_LONGITUDES[city];
    }
  }

  // 使用省份默认经度
  const cleanProvince = province.replace(/省$|自治区$|壮族自治区$|回族自治区$|维吾尔自治区$/g, '');
  if (PROVINCE_DEFAULT_LONGITUDE[cleanProvince]) {
    return PROVINCE_DEFAULT_LONGITUDE[cleanProvince];
  }
  if (PROVINCE_DEFAULT_LONGITUDE[province]) {
    return PROVINCE_DEFAULT_LONGITUDE[province];
  }

  return null;
}

/**
 * 计算真太阳时校正
 * @param longitude 出生地经度（东经）
 * @param clockHour 标准时间的小时
 * @param clockMinute 标准时间的分钟
 * @returns 校正后的真太阳时 { hour, minute }
 */
export function calculateTrueSolarTime(
  longitude: number,
  clockHour: number,
  clockMinute: number,
  clockSecond: number = 0,
): { hour: number; minute: number; second: number } {
  // 中国采用东经120度作为标准时间（北京时间）
  const standardLongitude = 120.0;

  // 计算经度差带来的时间差
  // 每度经度差4分钟
  // 东经120度以东的地方，真太阳时比北京时间早（需要减去时间差）
  // 东经120度以西的地方，真太阳时比北京时间晚（需要加上时间差）
  const longitudeDiffMinutes = (standardLongitude - longitude) * 4;

  // 将标准时间转换为总分钟数
  const totalMinutes = clockHour * 60 + clockMinute + clockSecond / 60;

  // 加上经度校正
  const correctedMinutes = totalMinutes + longitudeDiffMinutes;

  // 转换回时分
  let adjustedHour = Math.floor(correctedMinutes / 60);
  let adjustedMinute = Math.floor(correctedMinutes % 60);
  let adjustedSecond = Math.round((correctedMinutes % 1) * 60);

  // 处理秒数进位
  if (adjustedSecond >= 60) {
    adjustedSecond -= 60;
    adjustedMinute += 1;
  }
  if (adjustedMinute >= 60) {
    adjustedMinute -= 60;
    adjustedHour += 1;
  }
  if (adjustedHour >= 24) {
    adjustedHour -= 24;
  }
  if (adjustedHour < 0) {
    adjustedHour += 24;
  }

  return {
    hour: adjustedHour,
    minute: adjustedMinute,
    second: adjustedSecond,
  };
}

/**
 * 格式化时间校正信息
 */
export function formatTimeCorrection(
  longitude: number,
  originalHour: number,
  originalMinute: number,
  correctedHour: number,
  correctedMinute: number,
): string {
  const diffMinutes = Math.round((correctedHour * 60 + correctedMinute) - (originalHour * 60 + originalMinute));
  const diffSign = diffMinutes >= 0 ? '+' : '';
  const absDiff = Math.abs(diffMinutes);

  return `经度${longitude.toFixed(1)}°，真太阳时校正${diffSign}${Math.floor(absDiff / 60)}时${absDiff % 60}分`;
}
